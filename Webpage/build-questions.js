const bank = [];
const seenQ = new Set();
function add(d, q, options, correctText, e) {
  const opts = options.slice();
  if (new Set(opts).size !== opts.length) return;        // skip dup options
  if (!opts.includes(correctText)) return;
  const key = q.toLowerCase();
  if (seenQ.has(key)) return;
  seenQ.add(key);
  bank.push({ d, q, a: opts, c: opts.indexOf(correctText), e });
}

